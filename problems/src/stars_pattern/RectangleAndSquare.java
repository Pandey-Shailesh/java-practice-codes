package stars_pattern;

public class RectangleAndSquare {
public static void main(String args[])
{
	System.out.println("Rectangle");
	for(int a=10;a>0;a--) 
	{
		for(int b=0;b<10;b++)
		{
			System.out.print("*");
		}
		System.out.println(" ");
	}
	
	System.out.println();
	System.out.println("Square");
	
	for(int a=0;a<10;a++) 
	{
		for(int b=0;b<10;b++)
		{
			System.out.print(" *");
		}
		System.out.println(" ");
	}
	
	
	
	
	
}
}
