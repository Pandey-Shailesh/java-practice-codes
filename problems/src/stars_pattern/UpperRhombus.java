package stars_pattern;

public class UpperRhombus {
	public static void main (String args[]) 
	{
	int s=15;

		for(int a=1;a<=s;a++) 
		{
			for(int b=a;b<=s;b++)	
			{
				System.out.print(" ");
			}
			for(int c=0;c<=s;c++)
			{
			System.out.print("*");
			}
			System.out.println();
		}
		
		System.out.println();
		System.out.println();
		
		
		
		
		
		
		
		
		
		
	}

}
